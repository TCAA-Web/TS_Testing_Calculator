// Function to add two numbers
export const add = (num_a: number, num_b: number) => {
  return num_a + num_b
}

// Function to subtract two numbers
export const subtract = (num_a: number, num_b: number) => {
  return num_a - num_b
}

// Function to multiply two numbers
export const multiply = (num_a: number, num_b: number) => {
  return num_a * num_b
}

// Function to divide two numbers
export const divide = (num_a: number, num_b: number) => {
  return num_a / num_b
}
